public class for{
    public static void main ( String args[]){
        System.out.println("For!!");
        for ( int i = 1 ; i <= 10 ; i ++){
            System.out.println("******");
        }
        System.out.println("while!!");
        int a=1;
        while(a<=10){
            System.out.println("******");
            a++;
        }
    }
}